def match_coords(description: str, location: str) -> tuple[float, float]:
    """从描述和位置字段匹配最精确坐标，加随机扰动"""
    text = (description or "") + " " + (location or "")

    # 先查详细表（从最具体到最宽泛）
    for keywords, lat, lng, r in LOCATION_TABLE:
        if all(k in text for k in keywords):
            return (
                round(lat + random.uniform(-r, r), 5),
                round(lng + random.uniform(-r * 0.8, r * 0.8), 5),
            )

    # 再查作业区快速表
    for area, lat, lng, r in AREA_TABLE:
        if area in text:
            return (
                round(lat + random.uniform(-r, r), 5),
                round(lng + random.uniform(-r * 0.8, r * 0.8), 5),
            )

    # 兜底
    return (
        round(46.600 + random.uniform(-0.025, 0.025), 5),
        round(124.860 + random.uniform(-0.020, 0.020), 5),
    )


def run() -> None:
    conn = sqlite3.connect(str(DB_PATH))
    cur  = conn.cursor()

    cur.execute(
        "SELECT id, location, description FROM cases WHERE case_number LIKE 'AI202%'"
    )
    rows = cur.fetchall()
    print(f"待更新: {len(rows)} 条")

    updated = 0
    area_stats: dict[str, int] = {}

    for cid, loc, desc in rows:
        lat, lng = match_coords(desc or "", loc or "")
        cur.execute(
            "UPDATE cases SET latitude=?, longitude=? WHERE id=?",
            (lat, lng, cid),
        )
        # 统计分区
        key = "让胡路/齐家" if 46.5 < lat < 46.8 and 124.7 < lng < 125.0 \
            else "肇源县茂兴/新站" if 45.4 < lat < 45.8 and 124.6 < lng < 125.0 \
            else "杜蒙县北部" if lat > 46.8 \
            else "大同/红岗" if 46.0 < lat < 46.6 \
            else "其他"
        area_stats[key] = area_stats.get(key, 0) + 1
        updated += 1

    conn.commit()
    conn.close()

    print(f"✓ 更新 {updated} 条坐标")
    print("\n分布统计（粗略）:")
    for k, n in sorted(area_stats.items(), key=lambda x: -x[1]):
        print(f"  {k}: {n} 案")


if __name__ == "__main__":
    run()
